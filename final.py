def count_direction_changes(values, min_step=0.003):
    values = np.asarray(values, dtype=float)

    if len(values) < 3:
        return 0

    diffs = np.diff(values)
    diffs[np.abs(diffs) < min_step] = 0.0

    signs = np.sign(diffs)
    signs = signs[signs != 0]

    if len(signs) < 2:
        return 0

    changes = np.sum(signs[1:] != signs[:-1])

    return int(changes)


def classify_head_motion(video_path, preview=True, max_preview_frames=5):
    cap = cv2.VideoCapture(video_path)

    if not cap.isOpened():
        raise RuntimeError("Video could not be opened for head tracking.")

    fps = cap.get(cv2.CAP_PROP_FPS)

    if fps is None or fps <= 0:
        fps = 30.0

    face_cascade = cv2.CascadeClassifier(
        cv2.data.haarcascades + "haarcascade_frontalface_default.xml"
    )

    centers_x = []
    centers_y = []
    face_sizes = []
    preview_frames = []

    frame_index = 0
    sample_stride = max(1, int(round(fps / 10.0)))

    while True:
        ret, frame = cap.read()

        if not ret:
            break

        if frame_index % sample_stride != 0:
            frame_index += 1
            continue

        gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)

        faces = face_cascade.detectMultiScale(
            gray,
            scaleFactor=1.1,
            minNeighbors=3,
            minSize=(90, 90)
        )

        if len(faces) == 0:
            frame_index += 1
            continue

        faces = sorted(faces, key=lambda f: f[2] * f[3], reverse=True)
        face = faces[0]

        x, y, w, h = face

        cx = x + w / 2.0
        cy = y + h / 2.0

        centers_x.append(cx)
        centers_y.append(cy)
        face_sizes.append((w + h) / 2.0)

        if preview and len(preview_frames) < max_preview_frames:
            frame_preview = frame.copy()

            cv2.rectangle(
                frame_preview,
                (x, y),
                (x + w, y + h),
                (0, 255, 0),
                2
            )

            cv2.circle(
                frame_preview,
                (int(cx), int(cy)),
                6,
                (0, 0, 255),
                -1
            )

            cv2.putText(
                frame_preview,
                "Tracked head center",
                (x, max(0, y - 10)),
                cv2.FONT_HERSHEY_SIMPLEX,
                0.8,
                (0, 255, 0),
                2
            )

            preview_frames.append(frame_preview)

        frame_index += 1

    cap.release()

    centers_x = np.asarray(centers_x, dtype=float)
    centers_y = np.asarray(centers_y, dtype=float)
    face_sizes = np.asarray(face_sizes, dtype=float)

    if len(centers_x) < 8:
        return {
            "predicted_class": "stable",
            "confidence": 0.50,
            "features": {
                "x_amp": 0.0,
                "y_amp": 0.0,
                "total_amp": 0.0,
                "y_to_x_ratio": 0.0,
                "x_to_y_ratio": 0.0,
                "x_direction_changes": 0,
                "y_direction_changes": 0,
                "total_direction_changes": 0,
                "path_length": 0.0,
                "used_trim": False
            },
            "preview_frames": preview_frames
        }

    median_face_size = np.median(face_sizes)

    if median_face_size < 1e-8:
        median_face_size = 1.0

    x_norm = (centers_x - np.median(centers_x)) / median_face_size
    y_norm = (centers_y - np.median(centers_y)) / median_face_size

    used_trim = False

    if len(x_norm) > 20:
        trim_n = max(2, int(0.10 * len(x_norm)))
        x_used = x_norm[trim_n:-trim_n]
        y_used = y_norm[trim_n:-trim_n]
        used_trim = True
    else:
        x_used = x_norm
        y_used = y_norm

    x_amp = float(np.percentile(x_used, 95) - np.percentile(x_used, 5))
    y_amp = float(np.percentile(y_used, 95) - np.percentile(y_used, 5))
    total_amp = float(np.sqrt(x_amp ** 2 + y_amp ** 2))

    y_to_x_ratio = float(y_amp / (x_amp + 1e-8))
    x_to_y_ratio = float(x_amp / (y_amp + 1e-8))

    x_direction_changes = count_direction_changes(x_used)
    y_direction_changes = count_direction_changes(y_used)
    total_direction_changes = x_direction_changes + y_direction_changes

    dx = np.diff(x_used)
    dy = np.diff(y_used)
    path_length = float(np.sum(np.sqrt(dx ** 2 + dy ** 2)))

    # ------------------------------------------------------------
    # Original working decision logic
    # Do not modify this block unless you want to re-tune classification.
    # ------------------------------------------------------------

    if total_amp < 0.18 and path_length < 1.20:
        predicted_class = "stable"
        confidence = 0.90

    # Strong LEFT-RIGHT exception:
    # This is placed before the zigzag rule only for very clear horizontal motion.
    # It fixes left-right videos where noisy y tracking creates many false direction changes.
    # The y_amp and x_to_y_ratio guards prevent real zigzag videos from being stolen by left_right.
    elif x_amp >= 0.22 and x_to_y_ratio >= 4.00 and y_amp < 0.12:
        predicted_class = "left_right"
        confidence = 0.95

    elif y_direction_changes >= 8 and total_direction_changes >= 12:
        predicted_class = "zigzag"
        confidence = 0.95

    elif x_amp >= 0.22 and x_to_y_ratio >= 1.70:
        predicted_class = "left_right"
        confidence = 0.95

    elif total_direction_changes >= 12:
        predicted_class = "zigzag"
        confidence = 0.80

    elif x_amp > y_amp:
        predicted_class = "left_right"
        confidence = 0.75

    else:
        predicted_class = "stable"
        confidence = 0.70

    features = {
        "x_amp": x_amp,
        "y_amp": y_amp,
        "total_amp": total_amp,
        "y_to_x_ratio": y_to_x_ratio,
        "x_to_y_ratio": x_to_y_ratio,
        "x_direction_changes": x_direction_changes,
        "y_direction_changes": y_direction_changes,
        "total_direction_changes": total_direction_changes,
        "path_length": path_length,
        "used_trim": used_trim
    }

    return {
        "predicted_class": predicted_class,
        "confidence": confidence,
        "features": features,
        "preview_frames": preview_frames
    }


