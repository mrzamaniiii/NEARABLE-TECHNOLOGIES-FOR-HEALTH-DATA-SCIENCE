def keep_roi_inside_frame(roi, frame_shape):
    x, y, w, h = roi
    height, width = frame_shape[:2]

    x = max(0, x)
    y = max(0, y)
    w = max(1, min(w, width - x))
    h = max(1, min(h, height - y))

    return x, y, w, h


def get_multi_rois(face, frame_shape):
    x, y, w, h = face

    rois = {}

    rois["forehead"] = (
        x + int(0.30 * w),
        y + int(0.08 * h),
        int(0.40 * w),
        int(0.18 * h)
    )

    rois["left_cheek"] = (
        x + int(0.18 * w),
        y + int(0.48 * h),
        int(0.22 * w),
        int(0.20 * h)
    )

    rois["right_cheek"] = (
        x + int(0.60 * w),
        y + int(0.48 * h),
        int(0.22 * w),
        int(0.20 * h)
    )

    for key in rois:
        rois[key] = keep_roi_inside_frame(rois[key], frame_shape)

    return rois


def mean_rgb_from_roi(frame, roi):
    x, y, w, h = roi
    roi_frame = frame[y:y+h, x:x+w, :]

    if roi_frame.size == 0:
        return None

    mean_bgr = np.mean(roi_frame.reshape(-1, 3), axis=0)
    mean_rgb = mean_bgr[::-1]

    return mean_rgb


