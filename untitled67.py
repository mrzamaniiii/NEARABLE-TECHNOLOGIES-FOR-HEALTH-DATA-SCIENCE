def normalize_signal(x):
    x = np.asarray(x, dtype=float)
    return (x - np.nanmean(x)) / (np.nanstd(x) + 1e-8)


def remove_spikes(x, kernel_size=7, z_thresh=3.5):
    x = np.asarray(x, dtype=float)

    if len(x) < kernel_size:
        return x

    y = x.copy()
    med = medfilt(y, kernel_size=kernel_size)

    diff = y - med
    mad = np.median(np.abs(diff)) + 1e-8
    z = 0.6745 * diff / mad

    y[np.abs(z) > z_thresh] = med[np.abs(z) > z_thresh]

    return y


def bandpass_filter(x, fs, min_bpm=40, max_bpm=140, order=4):
    x = np.asarray(x, dtype=float)

    low = min_bpm / 60.0
    high = max_bpm / 60.0

    try:
        b, a = butter(order, [low, high], btype="bandpass", fs=fs)

        if len(x) <= 3 * max(len(a), len(b)):
            return x

        return filtfilt(b, a, x)

    except Exception as e:
        print("Filter warning:", e)
        return x


def estimate_hr_fft(signal, fs):
    signal = np.asarray(signal, dtype=float)

    if len(signal) < int(fs * 6):
        return None, 0.0

    x = signal - np.mean(signal)
    x = x * np.hanning(len(x))

    freqs = np.fft.rfftfreq(len(x), d=1.0 / fs)
    spec = np.abs(np.fft.rfft(x))

    bpm = freqs * 60.0
    mask = (bpm >= MIN_BPM) & (bpm <= MAX_BPM)

    if not np.any(mask):
        return None, 0.0

    bpm_valid = bpm[mask]
    spec_valid = spec[mask]

    idx = np.argmax(spec_valid)
    hr = float(bpm_valid[idx])

    peak_power = spec_valid[idx]
    median_power = np.median(spec_valid) + 1e-8
    confidence = float(peak_power / median_power)

    return hr, confidence


def estimate_hr_peak(signal, fs):
    signal = np.asarray(signal, dtype=float)

    if len(signal) < int(fs * 6):
        return None, 0.0

    x = signal - np.mean(signal)

    min_distance = max(1, int(fs * 60.0 / MAX_BPM))
    prominence = max(0.28 * np.std(x), 0.08)

    peaks, _ = find_peaks(
        x,
        distance=min_distance,
        prominence=prominence
    )

    if len(peaks) < 3:
        return None, 0.0

    intervals = np.diff(peaks) / fs
    bpms = 60.0 / intervals

    bpms = bpms[
        (bpms >= MIN_BPM) &
        (bpms <= MAX_BPM)
    ]

    if len(bpms) < 2:
        return None, 0.0

    hr = float(np.median(bpms))

    variability = np.std(bpms)
    confidence = float(1.0 / (1.0 + variability / 8.0))

    return hr, confidence


def weighted_median(values, weights):
    values = np.asarray(values, dtype=float)
    weights = np.asarray(weights, dtype=float)

    order = np.argsort(values)
    values = values[order]
    weights = weights[order]

    cumulative = np.cumsum(weights)
    cutoff = np.sum(weights) / 2.0

    return float(values[np.searchsorted(cumulative, cutoff)])


