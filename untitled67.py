def smart_select_hr(results):
    green_fft = results["green"]["fft_hr"]
    green_peak = results["green"]["peak_hr"]

    pos_fft = results["pos"]["fft_hr"]
    pos_peak = results["pos"]["peak_hr"]

    chrom_fft = results["chrom"]["fft_hr"]
    chrom_peak = results["chrom"]["peak_hr"]

    candidates = []

    for method in results:
        for key in ["fft", "peak"]:
            hr = results[method][f"{key}_hr"]
            conf = results[method][f"{key}_conf"]

            if hr is not None and MIN_BPM <= hr <= MAX_BPM:
                candidates.append((f"{method}_{key}", hr, conf))

    if len(candidates) == 0:
        return None, "failed"

    # ========================================================
    # Rule A: اگر Green FFT و Green Peak خیلی نزدیک‌اند
    # معمولاً برای ویدیوهای ثابت یا حرکت کم بهترین انتخاب است
    # ========================================================

    if green_fft is not None and green_peak is not None:
        if 50 <= green_fft <= 120 and 50 <= green_peak <= 120:
            if abs(green_fft - green_peak) <= 5:
                return (green_fft + green_peak) / 2.0, "green_fft_peak_agree"

    # ========================================================
    # Rule B: اگر CHROM FFT و CHROM Peak نزدیک‌اند
    # برای بعضی ویدیوهای متحرک بهتر جواب داده
    # ========================================================

    if chrom_fft is not None and chrom_peak is not None:
        if 50 <= chrom_fft <= 120 and 50 <= chrom_peak <= 120:
            if abs(chrom_fft - chrom_peak) <= 7:
                return (chrom_fft + chrom_peak) / 2.0, "chrom_fft_peak_agree"

    # ========================================================
    # Rule C: اگر POS FFT و POS Peak نزدیک‌اند
    # ========================================================

    if pos_fft is not None and pos_peak is not None:
        if 50 <= pos_fft <= 120 and 50 <= pos_peak <= 120:
            if abs(pos_fft - pos_peak) <= 7:
                return (pos_fft + pos_peak) / 2.0, "pos_fft_peak_agree"

    # ========================================================
    # Rule D: مشکل اصلی دیتاست تو:
    # FFT روی 42 تا 58 گیر می‌کند، ولی peakهای POS/CHROM نزدیک HR واقعی‌اند.
    # پس اگر حداقل دو FFT پایین داریم و peak بین 65 تا 95 داریم،
    # FFT پایین reject می‌شود.
    # ========================================================

    fft_values = [green_fft, pos_fft, chrom_fft]
    low_fft_count = sum(
        1 for x in fft_values
        if x is not None and 40 <= x < 60
    )

    rescue_peaks = []

    for name, hr, conf in candidates:
        if "peak" in name and 65 <= hr <= 95:
            rescue_peaks.append((name, hr, conf))

    if low_fft_count >= 2 and len(rescue_peaks) > 0:
        # اگر چند peak داریم، weighted median بگیر
        values = [x[1] for x in rescue_peaks]
        weights = [max(x[2], 0.2) for x in rescue_peaks]
        return weighted_median(values, weights), "peak_rescue_low_fft_failure"

    # ========================================================
    # Rule E: اگر POS/CHROM Peak باهم نزدیک‌اند، میانگین بگیر
    # ========================================================

    if pos_peak is not None and chrom_peak is not None:
        if 60 <= pos_peak <= 105 and 60 <= chrom_peak <= 105:
            if abs(pos_peak - chrom_peak) <= 10:
                return (pos_peak + chrom_peak) / 2.0, "pos_chrom_peak_agree"

    # ========================================================
    # Rule F: اگر یک peak منطقی بین 60 تا 100 داریم و FFTها پایین‌اند
    # ========================================================

    good_peaks = []

    for name, hr, conf in candidates:
        if "peak" in name and 60 <= hr <= 100:
            good_peaks.append((name, hr, conf))

    if len(good_peaks) > 0:
        best = max(good_peaks, key=lambda x: x[2])
        return best[1], f"{best[0]}_best_peak"

    # ========================================================
    # Rule G: weighted median از کاندیدهای فیزیولوژیک
    # ========================================================

    physiological = []

    for name, hr, conf in candidates:
        if 50 <= hr <= 110:
            physiological.append((name, hr, max(conf, 0.2)))

    if len(physiological) > 0:
        values = [x[1] for x in physiological]
        weights = [x[2] for x in physiological]
        return weighted_median(values, weights), "weighted_median_physiological"

    # Final fallback
    best = max(candidates, key=lambda x: x[2])
    return best[1], f"fallback_{best[0]}"


