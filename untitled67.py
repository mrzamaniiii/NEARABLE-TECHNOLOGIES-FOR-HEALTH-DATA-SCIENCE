def pos_rppg(rgb_signal, fs, window_seconds=1.6):
    rgb_signal = np.asarray(rgb_signal, dtype=float)
    n = len(rgb_signal)
    L = int(window_seconds * fs)

    h = np.zeros(n)

    if n < L:
        return h

    for start in range(0, n - L + 1):
        end = start + L

        C = rgb_signal[start:end].T
        mean_color = np.mean(C, axis=1, keepdims=True)
        Cn = C / (mean_color + 1e-8)

        S1 = Cn[1] - Cn[2]
        S2 = Cn[1] + Cn[2] - 2 * Cn[0]

        alpha = np.std(S1) / (np.std(S2) + 1e-8)

        H = S1 + alpha * S2
        H = H - np.mean(H)

        h[start:end] += H

    return h


def chrom_rppg(rgb_signal, fs, window_seconds=1.6):
    rgb_signal = np.asarray(rgb_signal, dtype=float)
    n = len(rgb_signal)
    L = int(window_seconds * fs)

    h = np.zeros(n)

    if n < L:
        return h

    for start in range(0, n - L + 1):
        end = start + L

        C = rgb_signal[start:end].T
        mean_color = np.mean(C, axis=1, keepdims=True)
        Cn = C / (mean_color + 1e-8)

        R = Cn[0]
        G = Cn[1]
        B = Cn[2]

        X = 3 * R - 2 * G
        Y = 1.5 * R + G - 1.5 * B

        alpha = np.std(X) / (np.std(Y) + 1e-8)

        H = X - alpha * Y
        H = H - np.mean(H)

        h[start:end] += H

    return h


