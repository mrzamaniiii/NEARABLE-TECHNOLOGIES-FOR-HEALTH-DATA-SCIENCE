def keep_roi_inside_frame(roi, frame_shape):
    x, y, w, h = roi
    H, W = frame_shape[:2]

    x = max(0, x)
    y = max(0, y)
    w = max(1, min(w, W - x))
    h = max(1, min(h, H - y))

    return x, y, w, h


def get_rois(face, frame_shape):
    x, y, w, h = face

    rois = {
        "forehead": (
            x + int(0.30 * w),
            y + int(0.08 * h),
            int(0.40 * w),
            int(0.18 * h)
        ),
        "left_cheek": (
            x + int(0.18 * w),
            y + int(0.52 * h),
            int(0.22 * w),
            int(0.18 * h)
        ),
        "right_cheek": (
            x + int(0.60 * w),
            y + int(0.52 * h),
            int(0.22 * w),
            int(0.18 * h)
        )
    }

    return {
        name: keep_roi_inside_frame(roi, frame_shape)
        for name, roi in rois.items()
    }


def mean_rgb_from_roi(frame, roi):
    x, y, w, h = roi
    patch = frame[y:y+h, x:x+w]

    if patch.size == 0:
        return None

    mean_bgr = np.mean(patch.reshape(-1, 3), axis=0)
    mean_rgb = mean_bgr[::-1]

    return mean_rgb


